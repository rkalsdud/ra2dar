import cv2
import numpy as np

# ====== 1. 수집한 좌표 데이터 입력 ======
# 주의: 사진 좌표와 레이다 좌표의 순서가 무조건 1:1로 맞아야 합니다!

image_points = np.array([
    [377, 429],   # 1번
    [493, 391],   # 2번
    [766, 233],  # 3번
    [990, 120],    # 4번
    [1244, 425],     # 5번
    [994, 459]
    # [460, 380],    # 1: 왼쪽 사람 몸통
    # [510, 470],    # 2: 검은 보더콜리
    # [1100, 600],   # 3: 흰 골든 머리 쪽
    # [930, 540],    # 4: 흰 골든 몸통 뒷부분
    # [770, 200],    # 5: 뒤 벽 로고
    # [1100, 400],   # 6: 오른쪽 뒤 벽
], dtype=np.float32)

radar_points = np.array([
    [-1.0036274194717407, 1.946988821029663, -1.0594732761383057],   # 1번
    [-1.6218929290771484, 3.9312211666107178, -1.2642860488891602],  # 2번
    [0.05224362760782242, 3.3616437435150146, -0.9722139835357666],  # 3번
    [0.903122067451477, 3.680251359939575, 0.16299059987068176], # 4번
    [1.232028603553772, 2.4827513694763184, -1.3370890617370605],  # 5번
    [0.8423493504524231, 3.6981630325317383, -0.022009527310729027]
    # [-1.297257661819458,   1.2542139291763306, -0.45413780212402344],
    # [-1.5352723598480225,  1.296464443206787,  -1.3720920085906982],
    # [ 1.2134737968444824,  1.3890827894210815, -1.6946489810943604],
    # [ 1.5440031290054321,  2.124570369720459,  -1.4617377519607544],
    # [-2.633314371109009,   3.3587918281555176,  0.9173222184181213],
    # [ 1.6883330345153809,  5.227502346038818,  -0.991105318069458],
], dtype=np.float32)

# ====== 2. 가상의 렌즈 설정 (왜곡 보정 생략 시 사용하는 추정치) ======
# 해상도 1280x720 기준 (다른 해상도라면 center 값을 절반으로 수정하세요)
center_x = 1280 / 2
center_y = 720 / 2
focal_length = 800.0 # 광각 렌즈의 대략적인 초점 거리

camera_matrix = np.array([
    [focal_length, 0, center_x],
    [0, focal_length, center_y],
    [0, 0, 1]
], dtype=np.float32)

# 왜곡이 없다고 가정 (0)
dist_coeffs = np.zeros((4, 1))

# ====== 3. 투영 행렬 계산 및 검증 ======
if len(image_points) < 4:
    print(f"⚠️ 현재 입력된 점: {len(image_points)}개")
    print("계산을 수행하려면 최소 4개 이상의 점 쌍이 필요합니다!")
else:
    # 캘리브레이션 핵심 함수 (3D -> 2D 변환 관계 도출)
    ret, rvec, tvec = cv2.solvePnP(radar_points, image_points, camera_matrix, dist_coeffs)
    
    print("✅ 계산 성공!\n")
    print("--- 회전 벡터 (rvec) ---")
    print(rvec)
    print("--- 이동 벡터 (tvec) ---")
    print(tvec)
    
    print("\n🔍 [정확도 검증] 레이다 점을 사진에 다시 찍어봅니다...")
    projected_points, _ = cv2.projectPoints(radar_points, rvec, tvec, camera_matrix, dist_coeffs)
    
    for i in range(len(image_points)):
        real_u, real_v = image_points[i]
        proj_u, proj_v = projected_points[i].ravel()
        
        # 실제 내가 찍은 픽셀과, 행렬이 계산한 픽셀의 오차 확인
        error_x = abs(real_u - proj_u)
        error_y = abs(real_v - proj_v)
        print(f"[{i+1}번 점] 실제 픽셀: ({real_u:4.0f}, {real_v:4.0f}) | 계산된 픽셀: ({proj_u:4.0f}, {proj_v:4.0f}) -> 오차: X {error_x:4.1f}px, Y {error_y:4.1f}px")